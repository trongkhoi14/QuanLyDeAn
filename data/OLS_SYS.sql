--OLS: nguoi quan ly cac thong bao la MYADMIN

ALTER SESSION SET CONTAINER = QLDAPDB;
EXEC LBACSYS.CONFIGURE_OLS;
EXEC LBACSYS.OLS_ENFORCEMENT.ENABLE_OLS;

--B1: Tao chinh sach
CONNECT LBACSYS/LBACSYS;
--EXECUTE SA_SYSDBA.DROP_POLICY('ACCESS_THONGBAO');
EXECUTE SA_SYSDBA.CREATE_POLICY(policy_name =>'ACCESS_THONGBAO', column_name =>'OLS_THONGBAO');
DISCONNECT LBACSYS;


--Gan quyen quan ly policy cho MYADMIN
CONNECT LBACSYS/LBACSYS;
GRANT ACCESS_THONGBAO_DBA TO MYADMIN;
--Tao thanh phan cua label
GRANT EXECUTE ON SA_COMPONENTS TO MYADMIN;
--Tao label
GRANT EXECUTE ON SA_LABEL_ADMIN TO MYADMIN;
--Gan policy cho bang
GRANT EXECUTE ON SA_POLICY_ADMIN TO MYADMIN;
--Gan label cho tai khoan
GRANT EXECUTE ON SA_USER_ADMIN TO MYADMIN;
--Chuyen chuoi thanh so cua label
GRANT EXECUTE ON CHAR_TO_LABEL TO MYADMIN;
DISCONNECT LBACSYS;



